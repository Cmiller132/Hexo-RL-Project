const fn build_pattern_counts() -> [(u8, u8); 729] {
    let mut table = [(0u8, 0u8); 729];
    let mut i = 0usize;
    while i < 729 {
        let mut idx = i;
        let mut p0 = 0u8;
        let mut p1 = 0u8;
        let mut j = 0;
        while j < 6 {
            let digit = idx % 3;
            if digit == 1 {
                p0 += 1;
            } else if digit == 2 {
                p1 += 1;
            }
            idx /= 3;
            j += 1;
        }
        table[i] = (p0, p1);
        i += 1;
    }
    table
}

pub const PATTERN_COUNTS: [(u8, u8); 729] = build_pattern_counts();

